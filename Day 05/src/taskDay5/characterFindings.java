package taskDay5;

public class characterFindings {
	public static void main(String[] args) {
		
		String s1 = "SoftwareTestEngineer";
		int indexOf = s1.indexOf('e');
		System.out.println("SoftwareTestEngineer :" + s1.charAt(indexOf));
		
		String s2 = "SeleniumAutomationtool";
		int indexOf2 = s2.indexOf('o');
		System.out.println("SeleniumAutomationtool :" + s2.charAt(indexOf2));
		
		String s3 = "Goodmorning";
		int indexOf3 = s3.indexOf('o');
		System.out.println("Goodmorning:" + s3.charAt(indexOf3));
		
		String s4 = "j a v a p r o g r a m";
		int indexOf4 = s4.indexOf('p');
		System.out.println("j a v a p r o g r a m :" + s4.charAt(indexOf4));
		
		String s5 = "9876543210";
		int indexOf5 = s5.indexOf('7');
		System.out.println("9876543210 :" + s5.charAt(indexOf5));
		
	}

}
